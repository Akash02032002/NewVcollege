    <script>
      $(document).ready(function () {
        function debounce(func, wait, immediate) {
          var timeout;
          return function () {
            var context = this,
              args = arguments;
            var later = function () {
              timeout = null;
              if (!immediate) func.apply(context, args);
            };
            var callNow = immediate && !timeout;
            clearTimeout(timeout);
            timeout = setTimeout(later, wait);
            if (callNow) func.apply(context, args);
          };
        }

        // Function to load teachers via AJAX
        function loadTeachers() {
          $("#loadingIndicator").show();
          $("#teacherCardsContainer").hide();

          $.ajax({
            url: "https://topcolleges.co.in",
            type: "GET",
            data: {
              search: $("#teacherSearch").val(),
              course: $("#courseFilter").val(),
              state: $("#stateFilter").val(),
              city: $("#cityFilter").val(),
              ajax: true,
            },
            success: function (data) {
              $("#teacherCardsContainer").html(data.teachers);
              $("#loadingIndicator").hide();
              $("#teacherCardsContainer").fadeIn();
            },
            error: function () {
              $("#loadingIndicator").hide();
              $("#teacherCardsContainer").html(
                '<div class="col-md-12"><div class="alert alert-danger">Error loading teachers. Please try again.</div></div>',
              );
              $("#teacherCardsContainer").fadeIn();
            },
          });
        }

        $("#teacherSearch").on("keyup", debounce(loadTeachers, 500));
        $("#courseFilter, #stateFilter, #cityFilter").on(
          "change",
          loadTeachers,
        );
      });
    </script>

    <script>
      $(document).ready(function () {
        let currentPage = 1;
        let isLoading = false;
        let hasMorePages = true;

        // Load more teachers when button clicked
        $("#loadMoreBtn").click(function () {
          if (!isLoading && hasMorePages) {
            loadMoreTeachers();
          }
        });

        function loadMoreTeachers() {
          isLoading = true;
          currentPage++;

          $("#loadMoreText").addClass("d-none");
          $("#loadMoreSpinner").removeClass("d-none");
          $("#loadingSpinner").show();

          const formData = {
            page: currentPage,
            search: $("#teacherSearch").val(),
            course: $("#courseFilter").val(),
            state: $("#stateFilter").val(),
            city: $("#cityFilter").val(),
            ajax: true,
          };

          $.ajax({
            url: "https://topcolleges.co.in",
            type: "GET",
            data: formData,
            success: function (response) {
              $("#teacherCardsContainer").append(response.teachers);
              $("#loadMoreContainer").html(response.pagination);

              hasMorePages = response.hasMorePages;
              if (!hasMorePages) {
                $("#loadMoreContainer").hide();
              }

              $(".panel-heading .badge").text(response.total);
            },
            error: function (xhr) {
              console.error("Error:", xhr.responseText);
              currentPage--;
            },
            complete: function () {
              isLoading = false;
              $("#loadMoreText").removeClass("d-none");
              $("#loadMoreSpinner").addClass("d-none");
              $("#loadingSpinner").hide();
            },
          });
        }
      });
    </script>